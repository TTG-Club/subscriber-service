package club.ttg.subscriber.domain.subscription.model;

/**
 * Способ появления подписки — нужен для аналитики и разграничения сценариев выдачи.
 */
public enum SubscriptionType {
    /** Подарок: подписка выдана бесплатно (например, по промо-коду). */
    GIFT,
    /** Покупка: подписка оплачена пользователем. */
    BUY
}
